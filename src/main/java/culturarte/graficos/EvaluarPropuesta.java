    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        if (!this.listaPropuestaProponente.isSelectionEmpty()) {
            String tituloProp = cortarTituloProp(this.listaPropuestaProponente.getSelectedValue());
            String evaluacion = this.comboEvaluacion.getSelectedItem().toString();
            EstadoPropuesta estProp;
            String aux;
            if (evaluacion.equals("Publicar")) {
                estProp = EstadoPropuesta.PUBLICADA;
                aux = "publicada";
            } else {
                estProp = EstadoPropuesta.CANCELADA;
                aux = "cancelada";
            }
            this.controller.cambiarEstadoPropuestaIngresada(tituloProp, estProp);
            JOptionPane.showMessageDialog(this, "Propuesta " + aux + " con éxito.",
                        "Éxito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una propuesta.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

