    private String cortarTituloProp(String tituloProp) {
        int primerEspacio = tituloProp.lastIndexOf(" - ");
        if (primerEspacio != -1) {
            tituloProp = tituloProp.substring(0, primerEspacio);
        }
        return tituloProp;
    }

